export function getTourBySlug(slug: string): TourData | undefined {
  return toursData[slug];
}

export function getAllTourSlugs(): string[] {
  return Object.keys(toursData);
}

export function getRelatedTours(currentSlug: string): TourData[] {
  const currentTour = getTourBySlug(currentSlug);
  if (!currentTour) return [];
  
  return currentTour.relatedTours
    .map(slug => getTourBySlug(slug))
    .filter((tour): tour is TourData => tour !== undefined);
}
